      <u><strong>HISTORY OF  THE SPORT.</strong></u>
      <pre>
          In the world,
          Handball is played in 199 countries of which 52 are in Africa. Locally, Handball was introduced in 1969. The Kenya Amateur Handball 
          Association (KAHA) was duly registered on <strong>24th May, 1972</strong>. Kenya joined Africa Confederation of Handball in the year 1980 and the
          International Handball Federation in 1982. The same year Kenya played an active role in the formation of the East and Central Africa Handball 
          Federation. In 1985, Kenya was a founder member of the Commonwealth Handball Association. The Association is also fully affiliated to the Kenya 
          National Sports Council (KNSC) and National Olympic of Kenya (NOCK).

